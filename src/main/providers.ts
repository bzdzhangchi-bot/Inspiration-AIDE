type RawOpenAIChoice = NonNullable<RawOpenAIChatCompletionResponse['choices']>[number];
type RawOpenAIMessageContent = NonNullable<RawOpenAIChoice['message']>['content'];

function isAgentToolName(value: unknown): value is AgentToolName {
  return typeof value === 'string' && AGENT_TOOL_NAMES.includes(value as AgentToolName);
}

